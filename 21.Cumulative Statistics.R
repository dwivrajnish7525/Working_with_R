#Cumulative Statistics
#cumsum() Function
x1<-c(2,4,6,8,10)
cumsum(x1)

#cummean() Function
library(dplyr)
x2 <- c(3, 5, 7, 2, 6)
x3 <- cummean(x2)
x3

#cummax() Function
x1<-c(2,9,6,3,4)
cummax(x1)

#cummin() Function
x1<-c(2,4,1,8,0)
cummin(x1)

#cumpro() Function
x1<-c(2,4,6,8,10)
cumprod(x1)